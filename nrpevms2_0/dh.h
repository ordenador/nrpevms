#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xE2,0xE1,0xC9,0x6B,0x9D,0x7C,0x2E,0x0B,0x03,0xF2,0xCA,0x1B,
		0xFF,0xD7,0xAD,0xB7,0x28,0x78,0x17,0x38,0x37,0x4D,0xC1,0xB2,
		0xA0,0x0F,0xF3,0x9C,0x0B,0xDA,0x55,0x22,0x5E,0x1E,0xD2,0xB7,
		0x58,0x3E,0x3D,0x10,0x14,0xE2,0x99,0xA1,0x9D,0x10,0x97,0x6D,
		0xAC,0xBB,0xBA,0x24,0xF4,0x69,0x1B,0x1C,0xE0,0xB5,0xE4,0x2E,
		0xE8,0xB2,0x03,0x53,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
